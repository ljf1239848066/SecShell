public class InnerClass {
    static int innerMethod(int from) {
        return from * from;
    }
    static int innerMethod1(int from) {
        return from + from;
    }
    static int innerMethod2(int from) {
        return from - from;
    }
    static int innerMethod3(int from) {
        return from / from;
    }
    static int innerMethod4(int from) {
        return from ^ from;
    }
}
